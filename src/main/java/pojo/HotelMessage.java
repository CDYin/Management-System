package pojo;

public class HotelMessage {
    private String HMnumber ;
    private String Ccomment ;
    private String Cevaluate ;
    private String FestivalMessage ;
    private String Discount ;
    private String DiningTime ;
    private String ServiceTime  ;
    private String HAnumber  ;
    private String UpdateTime  ;

    public String getHMnumber() {
        return HMnumber;
    }

    public void setHMnumber(String HMnumber) {
        this.HMnumber = HMnumber;
    }

    public String getCcomment() {
        return Ccomment;
    }

    public void setCcomment(String ccomment) {
        Ccomment = ccomment;
    }

    public String getCevaluate() {
        return Cevaluate;
    }

    public void setCevaluate(String cevaluate) {
        Cevaluate = cevaluate;
    }

    public String getFestivalMessage() {
        return FestivalMessage;
    }

    public void setFestivalMessage(String festivalMessage) {
        FestivalMessage = festivalMessage;
    }

    public String getDiscount() {
        return Discount;
    }

    public void setDiscount(String discount) {
        Discount = discount;
    }

    public String getDiningTime() {
        return DiningTime;
    }

    public void setDiningTime(String diningTime) {
        DiningTime = diningTime;
    }

    public String getServiceTime() {
        return ServiceTime;
    }

    public void setServiceTime(String serviceTime) {
        ServiceTime = serviceTime;
    }

    public String getHAnumber() {
        return HAnumber;
    }

    public void setHAnumber(String HAnumber) {
        this.HAnumber = HAnumber;
    }

    public String getUpdateTime() {
        return UpdateTime;
    }

    public void setUpdateTime(String updateTime) {
        UpdateTime = updateTime;
    }
}
