package pojo;

public class LivingCondition {
    private String HRnumber   ;
    private String Cnumber   ;
    private String CheckNumber   ;
    private String CheckTime   ;
    private String DepartureTime   ;
    private String Evaluate  ;
    private String LCnote    ;
    private String UpdateTime    ;
    private String UpdatePersonNumber      ;

    public String getHRnumber() {
        return HRnumber;
    }

    public void setHRnumber(String HRnumber) {
        this.HRnumber = HRnumber;
    }

    public String getCnumber() {
        return Cnumber;
    }

    public void setCnumber(String cnumber) {
        Cnumber = cnumber;
    }

    public String getCheckNumber() {
        return CheckNumber;
    }

    public void setCheckNumber(String checkNumber) {
        CheckNumber = checkNumber;
    }

    public String getCheckTime() {
        return CheckTime;
    }

    public void setCheckTime(String checkTime) {
        CheckTime = checkTime;
    }

    public String getDepartureTime() {
        return DepartureTime;
    }

    public void setDepartureTime(String departureTime) {
        DepartureTime = departureTime;
    }

    public String getEvaluate() {
        return Evaluate;
    }

    public void setEvaluate(String evaluate) {
        Evaluate = evaluate;
    }

    public String getLCnote() {
        return LCnote;
    }

    public void setLCnote(String LCnote) {
        this.LCnote = LCnote;
    }

    public String getUpdateTime() {
        return UpdateTime;
    }

    public void setUpdateTime(String updateTime) {
        UpdateTime = updateTime;
    }

    public String getUpdatePersonNumber() {
        return UpdatePersonNumber;
    }

    public void setUpdatePersonNumber(String updatePersonNumber) {
        UpdatePersonNumber = updatePersonNumber;
    }
}
