package pojo;

public class HotelRoomFacilities {
    private String HRFnumber   ;
    private String SurplusAccount   ;
    private String WIFI   ;
    private String Airconditioner   ;
    private String Heating   ;
    private String BathroomMatching  ;
    private String UpdateTime    ;
    private String UpdatePersonNumber    ;

    public String getHRFnumber() {
        return HRFnumber;
    }

    public void setHRFnumber(String HRFnumber) {
        this.HRFnumber = HRFnumber;
    }

    public String getSurplusAccount() {
        return SurplusAccount;
    }

    public void setSurplusAccount(String surplusAccount) {
        SurplusAccount = surplusAccount;
    }

    public String getWIFI() {
        return WIFI;
    }

    public void setWIFI(String WIFI) {
        this.WIFI = WIFI;
    }

    public String getAirconditioner() {
        return Airconditioner;
    }

    public void setAirconditioner(String airconditioner) {
        Airconditioner = airconditioner;
    }

    public String getHeating() {
        return Heating;
    }

    public void setHeating(String heating) {
        Heating = heating;
    }

    public String getBathroomMatching() {
        return BathroomMatching;
    }

    public void setBathroomMatching(String bathroomMatching) {
        BathroomMatching = bathroomMatching;
    }

    public String getUpdateTime() {
        return UpdateTime;
    }

    public void setUpdateTime(String updateTime) {
        UpdateTime = updateTime;
    }

    public String getUpdatePersonNumber() {
        return UpdatePersonNumber;
    }

    public void setUpdatePersonNumber(String updatePersonNumber) {
        UpdatePersonNumber = updatePersonNumber;
    }
}
