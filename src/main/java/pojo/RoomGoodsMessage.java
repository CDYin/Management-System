package pojo;

public class RoomGoodsMessage {
    private String RGnumber   ;
    private String HRnumber   ;
    private String UseAmount   ;
    private String DamageConditions   ;
    private String RGMnote   ;
    private String UpdateTime  ;
    private String UpdatePersonNumber    ;

    public String getRGnumber() {
        return RGnumber;
    }

    public void setRGnumber(String RGnumber) {
        this.RGnumber = RGnumber;
    }

    public String getHRnumber() {
        return HRnumber;
    }

    public void setHRnumber(String HRnumber) {
        this.HRnumber = HRnumber;
    }

    public String getUseAmount() {
        return UseAmount;
    }

    public void setUseAmount(String useAmount) {
        UseAmount = useAmount;
    }

    public String getDamageConditions() {
        return DamageConditions;
    }

    public void setDamageConditions(String damageConditions) {
        DamageConditions = damageConditions;
    }

    public String getRGMnote() {
        return RGMnote;
    }

    public void setRGMnote(String RGMnote) {
        this.RGMnote = RGMnote;
    }

    public String getUpdateTime() {
        return UpdateTime;
    }

    public void setUpdateTime(String updateTime) {
        UpdateTime = updateTime;
    }

    public String getUpdatePersonNumber() {
        return UpdatePersonNumber;
    }

    public void setUpdatePersonNumber(String updatePersonNumber) {
        UpdatePersonNumber = updatePersonNumber;
    }
}
