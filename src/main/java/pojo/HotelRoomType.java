package pojo;

public class HotelRoomType {
    private String HRTnumber    ;
    private String SurplusAccount   ;
    private String HRTname    ;
    private String HRYsize    ;
    private String Window    ;
    private String AvailableNumber   ;
    private String BedNumberandSize;
    private String UpdateTime    ;
    private String UpdatePersonNumber    ;

    public String getHRTnumber() {
        return HRTnumber;
    }

    public void setHRTnumber(String HRTnumber) {
        this.HRTnumber = HRTnumber;
    }

    public String getSurplusAccount() {
        return SurplusAccount;
    }

    public void setSurplusAccount(String surplusAccount) {
        SurplusAccount = surplusAccount;
    }

    public String getHRTname() {
        return HRTname;
    }

    public void setHRTname(String HRTname) {
        this.HRTname = HRTname;
    }

    public String getHRYsize() {
        return HRYsize;
    }

    public void setHRYsize(String HRYsize) {
        this.HRYsize = HRYsize;
    }

    public String getWindow() {
        return Window;
    }

    public void setWindow(String window) {
        Window = window;
    }

    public String getAvailableNumber() {
        return AvailableNumber;
    }

    public void setAvailableNumber(String availableNumber) {
        AvailableNumber = availableNumber;
    }

    public String getBedNumberandSize() {
        return BedNumberandSize;
    }

    public void setBedNumberandSize(String bedNumberandSize) {
        BedNumberandSize = bedNumberandSize;
    }

    public String getUpdateTime() {
        return UpdateTime;
    }

    public void setUpdateTime(String updateTime) {
        UpdateTime = updateTime;
    }

    public String getUpdatePersonNumber() {
        return UpdatePersonNumber;
    }

    public void setUpdatePersonNumber(String updatePersonNumber) {
        UpdatePersonNumber = updatePersonNumber;
    }
}
